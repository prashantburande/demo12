//que 1   Wap to swap two numbers
package ifelse;

public class swap {

	public static void main(String[] args) {
		int a=10;
        int b=20;
        System.out.println("before swap" + a+ " "+ " "+b);    
        int temp =a;
        a=b;
        b=temp;
        System.out.println("before swap" + a+ " "+ " "+b);
	}

}
